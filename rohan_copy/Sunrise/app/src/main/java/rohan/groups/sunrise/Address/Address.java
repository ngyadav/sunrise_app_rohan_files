package rohan.groups.sunrise.Address;

public class Address {
    public  String Name;
    public  String Flat;
    public  String Landmark;
    public  String Pushid;
    public  String Location;
    public  String LocationCoord;
    public  String Pincode;
    public  String Number;





    public Address(){}

    public Address(String Name,String Flat,String Landmark,String Pushid,String Location,String LocationCoord,String Pincode,String Number){

        this.Name=Name;
        this.Flat=Flat;
        this.Landmark=Landmark;
        this.Pushid=Pushid;
        this.Location=Location;
        this.LocationCoord=LocationCoord;
        this.Pincode=Pincode;
        this.Number=Number;
    }
}
