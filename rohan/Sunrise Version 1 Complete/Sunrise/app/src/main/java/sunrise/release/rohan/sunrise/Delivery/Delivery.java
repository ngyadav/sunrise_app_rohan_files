package sunrise.release.rohan.sunrise.Delivery;

public class Delivery {
    public  String Name;
    public  String Number;
    public  String Pincode;
    public  String Town;
    public  String Flat;
    public  String Area;
    public  String Landmark;
    public  String Userid;
    public  String Pushid;





    public Delivery(){}

    public Delivery(String Name,String Number,String Pincode,String Town,String Flat,String Area,String Landmark,String Userid,String Pushid){

        this.Name=Name;
        this.Number=Number;
        this.Pincode=Pincode;
        this.Town=Town;
        this.Flat=Flat;
        this.Area=Area;
        this.Landmark=Landmark;
        this.Userid=Userid;
        this.Pushid=Pushid;
    }
}
