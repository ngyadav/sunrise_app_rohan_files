package sunrise.release.rohan.sunrise.Cart;

public class Cart {
    public  String Image1;
    public  String Mrp;
    public  String OrderNo;
    public  String Rewards;
    public  String Date;
    public  String ProductName;
    public  String OrderStatus;
    public  String Userid;
    public  String Quantity;
    public  String Size;
    public  String Pushid;
    public  String Colour;




    public Cart(){}

    public Cart(String Image1,String Mrp,String OrderNo,String Rewards,String Date,String ProductName,String OrderStatus,String Userid,String Quantity,String Size,String Pushid,String Colour){

        this.Image1=Image1;
        this.Mrp=Mrp;
        this.OrderNo=OrderNo;
        this.Rewards=Rewards;
        this.Date=Date;
        this.ProductName=ProductName;
        this.OrderStatus=OrderStatus;
        this.Userid=Userid;
        this.Quantity=Quantity;
        this.Size=Size;
        this.Pushid=Pushid;
        this.Colour=Colour;
    }
}
